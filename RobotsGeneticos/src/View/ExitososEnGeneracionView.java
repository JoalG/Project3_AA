    public void fillCbRobotsExitosos(){
        String[] array = new String[this.robotsExitosos.size()];
        for(int i = 0; i < array.length; i++) {
            array[i] = "Robot " + Integer.toString(robotsExitosos.get(i)+1);
        }
        this.cbRobotsExitosos.setModel(new DefaultComboBoxModel<>(array));
    }    

